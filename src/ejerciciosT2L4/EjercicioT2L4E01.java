package ejerciciosT2L4;

public class EjercicioT2L4E01 {

	public static void main(String[] args) {

		// numero: valor del numero que se mostrara en pantalla
		byte numero = 0;

		// Inicio del bucle
		do {

			// Muestra el numero por pantalla
			System.out.println(numero);

			// Suma 1 al numero
			++numero;

			// Mientras que el numero sea mayor o igual que 20

		} while (numero <= 20); // Fin del bucle

	}

}
