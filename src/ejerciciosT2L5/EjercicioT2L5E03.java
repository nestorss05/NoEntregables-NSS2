package ejerciciosT2L5;

public class EjercicioT2L5E03 {

	public static void main(String[] args) {

		// numero: variable usada como contador en el for
		int numero;
		
		// For: El numero empieza en 7 (puesto a que 1 es impar), debera ser menor que
		// 100, y se ira sumando 7 en cada iteracion
		for (numero = 7; numero <= 100; numero = numero + 7) {

			// Muestra el numero por pantalla
			System.out.println(numero);

		} // Fin For

	}

}
