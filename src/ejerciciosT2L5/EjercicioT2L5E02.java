package ejerciciosT2L5;

public class EjercicioT2L5E02 {

	public static void main(String[] args) {

		// numero: variable usada como contador en el for
		int numero;

		// For: El numero empieza en 2 (puesto a que 1 es impar), debera ser menor que
		// 200, y se ira sumando 2 en cada iteracion
		for (numero = 2; numero <= 200; numero = numero + 2) {

			// Muestra el numero por pantalla
			System.out.println(numero);

		} // Fin For

	}

}
